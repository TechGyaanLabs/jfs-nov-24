package com.careerit.cj.day8;

public class PreAndPostIncrement {

    public static void main(String[] args) {

        int num = 5;

        System.out.println(num++);
        System.out.println(++num);

        int num1 = 10;
        System.out.println(num1 + 1);
        System.out.println(num1);

    }
}
