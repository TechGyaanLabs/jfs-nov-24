package com.careerit.cj.day3;

public class RelationOperators {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
        System.out.println("a >= b : " + (a >= b));
        System.out.println("a <= b : " + (a <= b));
        System.out.println("a == b : " + (a == b));
        System.out.println("a != b : " + (a != b));

    }
}
